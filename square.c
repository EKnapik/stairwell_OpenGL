GLshort* getSquareConnectData( void )
{
    GLuint numVerts = getSquareNumVert();
    GLshort *connectData = malloc( sizeof( GLshort ) * numVerts );
    if( connectData == NULL )
    {
        perror( "could not malloc connect data for square\n" );
        exit( 1 );
    }

    for( int i = 0; i < numVerts; i++ )
    {
        connectData[i] = i;
    }

    return connectData;
}
